<template>
    <div class="fixed pt-0 md:pt-7 lg:pt-10 w-screen h-screen overflow-scroll " v-show="showItem">
        <div class="items-center mx-auto max-w-6xl mb-10 rounded-md bg-blue-100 dark:bg-gray-700 ">
            <div class="bg-blue-200 dark:bg-blue-800 px-10 py-3 text-xl font-mono tracking-wider rounded-md flex flex-col md:flex-row justify-between items-center relative">
                <div>
                    <span class="text-2xl">{{ product.brand.brandName }}: </span>{{ product.productName }}
                </div>
                <div class="flex mt-4 md: md:m-0 text-sm">
                    <div class="flex items-center bg-green-600 p-1 mx-3 px-3 cursor-pointer rounded-md text-white" @click="editItem"><span class="material-icons">edit</span>Edit</div>
                    <div class="flex items-center bg-red-600 p-1 mx-3 px-3 md:mr-10 cursor-pointer rounded-md text-white" @click="deleteItem"><span class="material-icons">delete</span>Delete</div>
                    <span class="material-icons p-2 bg-white text-black rounded-full cursor-pointer absolute top-2 right-2" @click="close">close</span>
                </div>
            </div>

            <div class="p-10 lg:mx-auto grid gap-3 grid-cols-1 md:grid-cols-2">
                <div class="flex justify-center md:flex-col">
                    <img :src="itemImgTest" alt="" class=" sm:max-h-96 bg-white" />
                    <div class="flex flex-col md:flex-row items-center justify-center bg-gray-400">
                        <div
                            class="w-7 h-7 m-2 text-center rounded-md cursor-pointer flex items-center justify-center"
                            v-for="color in product.colors"
                            :key="color.colorId"
                            :style="{ backgroundColor: color.hexColor }"
                        />
                    </div>
                </div>

                <div class="pb-8 sm:py-5 px-5">
                    <div class="pb-3 text-2xl border-b b mb-3">{{ product.productName }}</div>
                    <div class="px-3">
                        <div class="flex justify-between items-center mb-5">
                            <span class="text-md font-light">Warranty : {{ product.warranty == 0 ? "none" : product.warranty + " year" }}</span>
                            <span class="text-2xl text-red-500 font-bold">à¸¿ {{ product.price }}</span>
                        </div>
                        <p>{{ product.description }}</p>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
export default {
    name: "Item",
    components: {},
    props: {
        type: String,
        productId: String,
        itemImgTest: String,
        products:[]
    },
    data() {
        return {
            product: null,
            showItem: true,
            urlItem: "http://localhost:9091/product",
        };
    },
    methods: {
        close() {
            this.showItem = false;
            this.$router.push({
                name: "ProductTypes",
                params: { type: this.product.type.typeName },
            });
        },
        deleteItem() {
            let confirm = window.confirm("Are you sure?");
            if (confirm) {
                fetch(`${this.urlItem}/delete/${this.product.productId}`, { method: "DELETE" })
                    .then(() => {
                        this.close();
                    })
                    .catch((error) => console.log(error));
            }
            this.$emit("deleted-item", this.product);
        },
        editItem() {
            this.$router.push({
                name: "Form",
                params: { itemId: this.product.productId },
            });
        },
        async getProduct(id) {
            return fetch(`${this.urlItem}/${id}`)
                .then((res) => res.json())
                .catch((error) => console.log(error));
        },
    },
    async created() {
        this.product = await this.getProduct(this.productId);
    },
};
</script>
